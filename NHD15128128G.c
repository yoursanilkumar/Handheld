void NHD15128128G_SPI_Init(void)
{
		
		/* Configure PB0 as output */
		ROM_SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOD);
		
		ROM_GPIOPinTypeGPIOOutput(GPIO_PORTD_BASE, GPIO_PIN_2);
		ROM_GPIOPinWrite(GPIO_PORTD_BASE, GPIO_PIN_2, GPIO_PIN_2);	
		
		ROM_GPIOPinTypeGPIOOutput(GPIO_PORTD_BASE, GPIO_PIN_3);
		ROM_GPIOPinWrite(GPIO_PORTD_BASE, GPIO_PIN_3, GPIO_PIN_3);
		
		
		UARTprintf("NHD15128128G_SPI_Init\r\n");

		ROM_SysCtlPeripheralEnable(SYSCTL_PERIPH_SSI0);
		ROM_SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOA);
		/* Configure PB5 as output */
		ROM_GPIOPinTypeGPIOOutput(GPIO_PORTA_BASE, GPIO_PIN_3);
		ROM_GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_3, GPIO_PIN_3);	

		/* Enable the peripherals used to drive the SDC on SSI */
		ROM_SysCtlPeripheralEnable(SDC_SSI_FLASH_SYSCTL_PERIPH);
		ROM_SysCtlPeripheralEnable(SDC_GPIO_SYSCTL_FLASH_PERIPH);

		/*
		 * Configure the appropriate pins to be SSI instead of GPIO. The FSS (CS)
		 * signal is directly driven to ensure that we can hold it low through a
		 * complete transaction with the SD card.
		 */
		ROM_GPIOPinTypeSSI(SDC_GPIO_PORT_FLASH_BASE, SDC_SSI_FLASH_TX | SDC_SSI_FLASH_RX | SDC_SSI_FLASH_CLK | SDC_SSI_FLASH_FSS);
		ROM_GPIOPinTypeGPIOOutput(SDC_GPIO_PORT_FLASH_BASE, SDC_SSI_FLASH_FSS);

		ROM_GPIOPinConfigure(GPIO_PA2_SSI0CLK);
		ROM_GPIOPinConfigure(GPIO_PA3_SSI0FSS);
		ROM_GPIOPinConfigure(GPIO_PA4_SSI0RX);
		ROM_GPIOPinConfigure(GPIO_PA5_SSI0TX);	

		//ROM_GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_2, GPIO_PIN_2);
		/* Configure the SSI0 port */ //
		                                           
		ROM_SSIConfigSetExpClk(SDC_SSI_FLASH_BASE, 12000000, SSI_FRF_MOTO_MODE_3, SSI_MODE_MASTER, 4000000, 8);
											 
		ROM_SSIEnable(SDC_SSI_FLASH_BASE);

		CS_HIGH;          
 
}
